package com.codingblackfemales;

public class Calculator {
    public static Float add(final float firstInput, final float secondInput) {
        return firstInput + secondInput;
    }

    public static Float subtract(final float firstInput, final float secondInput) {
        return firstInput - secondInput;
    }

    public static Float multiply(final float firstInput, final float secondInput) {
        return firstInput * secondInput;
    }

    public static Float divide(final float firstInput, float secondInput) { return firstInput / secondInput;

    }
}
